import type { PgTestClient } from 'pgsql-test';
import { ident } from '../core/identifiers';

// ─── Global (constructive_memberships_public.*) ─────────────────────────────

/**
 * Set GLOBAL app_membership_defaults (is_verified, is_approved).
 *
 * This affects membership creation at the PLATFORM level (constructive_memberships_public).
 * For per-database defaults, use setDatabaseAppMembershipDefaults() instead.
 */
export async function setAppMembershipDefaults(
  pg: PgTestClient,
  options: { is_verified: boolean; is_approved: boolean }
): Promise<void> {
  await pg.query(
    `UPDATE constructive_memberships_public.app_membership_defaults
     SET is_verified = $1, is_approved = $2`,
    [options.is_verified, options.is_approved]
  );
}

/**
 * Make a user admin at the GLOBAL level (constructive_memberships_public.app_memberships).
 *
 * NOTE: RLS policies in provisioned databases check the DATABASE-LOCAL SPRT,
 * not this global table. Use makeDatabaseAppAdmin() for RLS testing.
 * This is only useful for platform-level operations.
 */
export async function makeAppAdmin(
  pg: PgTestClient,
  actor_id: string
): Promise<void> {
  await pg.query(
    `UPDATE constructive_memberships_public.app_memberships
     SET is_admin = TRUE
     WHERE actor_id = $1`,
    [actor_id]
  );
}

// ─── Database-Local (resolved via memberships_module in metaschema) ─────────

/**
 * Set database-level app membership defaults so that seedDatabaseUser
 * creates active memberships (is_active = is_approved AND is_verified).
 *
 * The module generator sets is_verified=FALSE and is_approved=FALSE by
 * default (secure defaults). Without this call, the SPRT insert trigger
 * sees is_active=FALSE and skips the SPRT row — so no user passes
 * AuthzAppMembership.
 *
 * Call this AFTER provisionDatabase and BEFORE seedDatabaseUser.
 */
export async function setDatabaseAppMembershipDefaults(
  pg: PgTestClient,
  database_id: string,
  options: { is_verified: boolean; is_approved: boolean }
): Promise<void> {
  const defaults = await pg.one<{ schema_name: string; table_name: string }>(
    `SELECT s.schema_name, t.name AS table_name
     FROM metaschema_modules_public.memberships_module mm
     JOIN metaschema_public.table t ON t.id = mm.membership_defaults_table_id
     JOIN metaschema_public.schema s ON s.id = t.schema_id
     WHERE mm.database_id = $1 AND mm.scope = 'app'`,
    [database_id]
  );

  await pg.query(
    `UPDATE ${ident(defaults.schema_name, defaults.table_name)}
     SET is_verified = $1, is_approved = $2`,
    [options.is_verified, options.is_approved]
  );
}

/**
 * Make a user an app-level admin in the DATABASE-LOCAL memberships table.
 *
 * RLS policies generated by apply_module_security / apply_agent_security / etc.
 * check the per-database app_memberships_sprt (not the global
 * constructive_memberships_* tables). This helper resolves the local
 * memberships table via memberships_module and sets is_admin = TRUE,
 * which the SPRT update trigger propagates to the local SPRT row.
 *
 * Call this AFTER seedDatabaseUser (so the membership row exists).
 */
export async function makeDatabaseAppAdmin(
  pg: PgTestClient,
  database_id: string,
  actor_id: string
): Promise<void> {
  const mem = await pg.one<{ schema_name: string; table_name: string }>(
    `SELECT s.schema_name, t.name AS table_name
     FROM metaschema_modules_public.memberships_module mm
     JOIN metaschema_public.table t ON t.id = mm.memberships_table_id
     JOIN metaschema_public.schema s ON s.id = t.schema_id
     WHERE mm.database_id = $1 AND mm.scope = 'app'`,
    [database_id]
  );

  await pg.query(
    `UPDATE ${ident(mem.schema_name, mem.table_name)}
     SET is_admin = TRUE
     WHERE actor_id = $1`,
    [actor_id]
  );
}
